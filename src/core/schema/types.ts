// Layout components
// ────────────────────────────────────────────────────────────────────────────

/** A Section accessory is either a single Button or a Thumbnail. */
export type SectionAccessory = ButtonComponent | ThumbnailComponent;

export interface SectionComponent extends BaseComponent {
  type: typeof ComponentType.Section;
  /** 1–3 TextDisplay children. */
  components: TextDisplayComponent[];
  accessory: SectionAccessory;
}

/**
 * Action rows hold EITHER up to 5 buttons OR exactly one select component.
 * The two shapes never mix — Discord rejects the row otherwise. We model both
 * variants in a single component so the editor can swap content without
 * recreating the row.
 */
export interface ActionRowComponent extends BaseComponent {
  type: typeof ComponentType.ActionRow;
  components: ButtonComponent[] | [SelectComponent];
}

/**
 * Top-level container. Renders with a left accent stripe in Discord, similar
 * to the legacy embed look. Cannot nest another Container.
 */
export interface ContainerComponent extends BaseComponent {
  type: typeof ComponentType.Container;
  /**
   * RGB integer (0xRRGGBB) or null. Discord renders no accent stripe when null.
   * We keep `undefined` and `null` distinct so the wire payload only emits the
   * field when the user explicitly chose a color.
   */
  accent_color?: number | null;
  spoiler?: boolean;
  components: ContainerChild[];
}

/** Components allowed inside a Container (no nested Container). */
export type ContainerChild =
  | ActionRowComponent
  | TextDisplayComponent
  | SectionComponent
  | MediaGalleryComponent
  | SeparatorComponent
  | FileComponent;

/** Top-level components allowed on a Components V2 message. */
export type TopLevelComponent =
  | ActionRowComponent
  | SectionComponent
  | TextDisplayComponent
  | MediaGalleryComponent
  | SeparatorComponent
  | FileComponent
  | ContainerComponent;

/** Any component (used for tree traversal). */
export type AnyComponent =
  | TopLevelComponent
  | ContainerChild
  | ButtonComponent
  | ThumbnailComponent
  | SectionAccessory
  | SelectComponent;

// ────────────────────────────────────────────────────────────────────────────
// Message-level options
// ────────────────────────────────────────────────────────────────────────────

/**
 * `allowed_mentions` controls which mentions in TextDisplay content resolve
 * to real pings. Without it, an `@everyone` token in a TextDisplay pings the
 * channel. Each `parse` entry whitelists a class of mention; the explicit
 * `roles`/`users` arrays whitelist specific snowflakes.
 *
 * Discord rejects a payload that supplies BOTH `parse: ["roles"]` and a
 * `roles: [...]` array (mutually exclusive); the same rule applies to users.
 */
export type AllowedMentionType = "roles" | "users" | "everyone";

export interface AllowedMentions {
  parse?: AllowedMentionType[];
  roles?: string[];
  users?: string[];
  /** Whether the author of the referenced message gets pinged on a reply. */
  replied_user?: boolean;
}

/**
 * Forge a reply by pointing at another message. For webhook execute Discord
 * accepts `message_id` (required) plus optional `channel_id` and
 * `fail_if_not_exists`. `type` defaults to 0 (default reply).
 */
export interface MessageReference {
  message_id: string;
  channel_id?: string;
  guild_id?: string;
  type?: number;
  fail_if_not_exists?: boolean;
}

/**
 * Message-level metadata. With Components V2, `content` and `embeds` are
 * forbidden — only `components` ship to Discord. Webhook execution params
 * (username, avatar_url, allowed_mentions, message_reference, thread_name,
 * applied_tags, suppress_notifications) live alongside for the executor to
 * consume.
 *
 * `flags` is computed at send time — IS_COMPONENTS_V2 is always present, and
 * `suppress_notifications` (1 << 12) is OR-ed in when the user enables it.
 */
export interface WebhookMessage {
  /** Override webhook display name. */
  username?: string;
  /** Override webhook avatar URL. */
  avatar_url?: string;
  /** Whether to read message content with TTS. */
  tts?: boolean;
  /** Send silently — recipients see the message but get no notification. */
  suppress_notifications?: boolean;
  /** Mention-resolution policy. See `AllowedMentions`. */
  allowed_mentions?: AllowedMentions;
  /** Reply to another message (forum/channel reply target). */
  message_reference?: MessageReference;
  /**
   * Name of the thread to create when posting into a forum/media channel.
   * Required for forum-channel posts; ignored elsewhere.
   */
  thread_name?: string;
  /**
   * Forum tags to apply to the new thread. Only honoured when posting to a
   * forum channel with `thread_name` set.
   */
  applied_tags?: string[];
  /** Top-level component list. Order matters — this is the render order. */
  components: TopLevelComponent[];
}

/**
 * Message flag that tells Discord this message uses the Components V2 layout
 * system. Must be set when posting; the editor always emits it.
 */
export const MESSAGE_FLAG_IS_COMPONENTS_V2 = 1 << 15;

/**
 * Silent-send flag. When set, recipients see the message but receive no
 * notification (mobile push, desktop toast, mention badge).
 */
export const MESSAGE_FLAG_SUPPRESS_NOTIFICATIONS = 1 << 12;

/**
 * Compute the `flags` integer Discord expects on the wire. `IS_COMPONENTS_V2`
 * is always present — every message this app emits is V2, and Discord rejects
 * V2 components when the flag is absent — with `SUPPRESS_NOTIFICATIONS` OR-ed
 * in when the user enabled silent send. This is the single source of truth for
 * flags across send, JSON export, and share-link serialization.
 */
export function computeMessageFlags(message: WebhookMessage): number {
  let flags = MESSAGE_FLAG_IS_COMPONENTS_V2;
  if (message.suppress_notifications) flags |= MESSAGE_FLAG_SUPPRESS_NOTIFICATIONS;
  return flags;
}

/** Inverse of {@link computeMessageFlags}: read the silent-send bit back. */
export function flagsHaveSuppressNotifications(flags: unknown): boolean {
  const n = Number(flags);
  return Number.isFinite(n) && (n & MESSAGE_FLAG_SUPPRESS_NOTIFICATIONS) !== 0;
}
