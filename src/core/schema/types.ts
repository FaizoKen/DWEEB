/**
 * Discord Components V2 schema.
 *
 * Types here mirror the wire format Discord expects when posting to a webhook
 * with the `IS_COMPONENTS_V2` message flag (1 << 15) set. They are intentionally
 * close to the official API shape so that `serialization/encode.ts` can emit a
 * payload Discord accepts with minimal transformation.
 *
 * Editor-only fields (an `_id` we generate for selection/diffing) are stripped
 * before export — see `serialization/normalize.ts`. Discord's own optional
 * per-component `id` integer (a separate field used for stable identity across
 * edits) is preserved as `componentId` and round-trips untouched.
 *
 * Reference: https://docs.discord.com/developers/components/reference
 */

/** Discord component type discriminator. */
export const ComponentType = {
  ActionRow: 1,
  Button: 2,
  StringSelect: 3,
  TextInput: 4,
  UserSelect: 5,
  RoleSelect: 6,
  MentionableSelect: 7,
  ChannelSelect: 8,
  Section: 9,
  TextDisplay: 10,
  Thumbnail: 11,
  MediaGallery: 12,
  File: 13,
  Separator: 14,
  Container: 17,
} as const;

export type ComponentTypeValue = (typeof ComponentType)[keyof typeof ComponentType];

/** Discord button style discriminator. */
export const ButtonStyle = {
  Primary: 1,
  Secondary: 2,
  Success: 3,
  Danger: 4,
  Link: 5,
  Premium: 6,
} as const;

export type ButtonStyleValue = (typeof ButtonStyle)[keyof typeof ButtonStyle];

/** Separator vertical spacing. */
export const SeparatorSpacing = {
  Small: 1,
  Large: 2,
} as const;

export type SeparatorSpacingValue = (typeof SeparatorSpacing)[keyof typeof SeparatorSpacing];

/**
 * Discord channel types accepted as `channel_types` filters on a Channel Select.
 * Numeric values mirror the API; the editor only surfaces the common ones in
 * the picker but accepts any integer for round-trip safety on import.
 */
export const ChannelType = {
  GuildText: 0,
  DM: 1,
  GuildVoice: 2,
  GroupDM: 3,
  GuildCategory: 4,
  GuildAnnouncement: 5,
  AnnouncementThread: 10,
  PublicThread: 11,
  PrivateThread: 12,
  GuildStageVoice: 13,
  GuildDirectory: 14,
  GuildForum: 15,
  GuildMedia: 16,
} as const;

export type ChannelTypeValue = (typeof ChannelType)[keyof typeof ChannelType];

/**
 * Editor-only identifier attached to every component so the builder can track
 * selection and reorder without relying on array indices. Stripped at export.
 */
export type EditorId = string;

/** Common fields present on every editor component. */
export interface BaseComponent {
  /** Editor-only stable id. Not sent to Discord. */
  _id: EditorId;
  /**
   * Discord's optional per-component 32-bit identifier. Auto-generated by
   * Discord when omitted; preserved verbatim on import/restore so callers
   * referencing components by id keep working.
   */
  id?: number;
  type: ComponentTypeValue;
}

/** Partial emoji used on buttons / select options. */
export interface PartialEmoji {
  id?: string | null;
  name?: string | null;
  animated?: boolean;
}

/**
 * Unfurled media item used by thumbnails, galleries, and files.
 *
 * Discord accepts either an absolute `url` (https:// or `attachment://<filename>`)
 * or an `attachment_id` snowflake referring to an already-uploaded attachment.
 * Restored messages also include resolved fields (`proxy_url`, `height`,
 * `width`, `content_type`, `loading_state`) which we preserve untouched but
 * do not render or edit directly.
 */
export interface UnfurledMediaItem {
  url?: string;
  attachment_id?: string;
  proxy_url?: string;
  height?: number | null;
  width?: number | null;
  content_type?: string;
  loading_state?: number;
}

// ────────────────────────────────────────────────────────────────────────────
// Leaf components
// ────────────────────────────────────────────────────────────────────────────

export interface TextDisplayComponent extends BaseComponent {
  type: typeof ComponentType.TextDisplay;
  content: string;
}

export interface ThumbnailComponent extends BaseComponent {
  type: typeof ComponentType.Thumbnail;
  media: UnfurledMediaItem;
  description?: string;
  spoiler?: boolean;
}

export interface MediaGalleryItem {
  media: UnfurledMediaItem;
  description?: string;
  spoiler?: boolean;
}

export interface MediaGalleryComponent extends BaseComponent {
  type: typeof ComponentType.MediaGallery;
  items: MediaGalleryItem[];
}

/**
 * Unlike Thumbnail / MediaGallery, a File's media only renders an *uploaded*
 * attachment — its `url` must use the `attachment://<filename>` form (or carry
 * an `attachment_id`). Discord rejects an external https URL here. Validation
 * enforces this; the editor's in-session `session://` refs rewrite to
 * `attachment://` at send time.
 */
export interface FileComponent extends BaseComponent {
  type: typeof ComponentType.File;
  file: UnfurledMediaItem;
  spoiler?: boolean;
}

export interface SeparatorComponent extends BaseComponent {
  type: typeof ComponentType.Separator;
  divider?: boolean;
  spacing?: SeparatorSpacingValue;
}

/**
 * Buttons split into two structural variants:
 *  - URL/Premium buttons carry a target (`url` or `sku_id`) and never a custom_id.
 *  - Interactive buttons carry a `custom_id` and a non-link style.
 *
 * Webhook messages can include interactive buttons, but interactions cannot be
 * handled without a backend. We still allow editing them so users can ship
 * messages a separate bot will receive.
 */
export interface LinkButtonComponent extends BaseComponent {
  type: typeof ComponentType.Button;
  style: typeof ButtonStyle.Link;
  label?: string;
  emoji?: PartialEmoji;
  url: string;
  disabled?: boolean;
}

export interface PremiumButtonComponent extends BaseComponent {
  type: typeof ComponentType.Button;
  style: typeof ButtonStyle.Premium;
  sku_id: string;
  disabled?: boolean;
}

export interface InteractiveButtonComponent extends BaseComponent {
  type: typeof ComponentType.Button;
  style:
    | typeof ButtonStyle.Primary
    | typeof ButtonStyle.Secondary
    | typeof ButtonStyle.Success
    | typeof ButtonStyle.Danger;
  label?: string;
  emoji?: PartialEmoji;
  custom_id: string;
  disabled?: boolean;
}

export type ButtonComponent =
  | LinkButtonComponent
  | PremiumButtonComponent
  | InteractiveButtonComponent;

// ────────────────────────────────────────────────────────────────────────────
// Select components (types 3, 5, 6, 7, 8)
//
// All five are interactive — Discord delivers their values via an interaction
// payload which only a bot can receive. The editor still exposes them so
// users can lay out a message that a bot they own will later process.
// ────────────────────────────────────────────────────────────────────────────

/** Option entry on a StringSelect. */
export interface StringSelectOption {
  label: string;
  value: string;
  description?: string;
  emoji?: PartialEmoji;
  default?: boolean;
}

interface BaseSelectComponent extends BaseComponent {
  custom_id: string;
  placeholder?: string;
  min_values?: number;
  max_values?: number;
  disabled?: boolean;
}

export interface StringSelectComponent extends BaseSelectComponent {
  type: typeof ComponentType.StringSelect;
  options: StringSelectOption[];
}

/** Default-value entries for the snowflake-resolving selects. */
export interface DefaultValueUser {
  id: string;
  type: "user";
}
export interface DefaultValueRole {
  id: string;
  type: "role";
}
export interface DefaultValueChannel {
  id: string;
  type: "channel";
}

export interface UserSelectComponent extends BaseSelectComponent {
  type: typeof ComponentType.UserSelect;
  default_values?: DefaultValueUser[];
}

export interface RoleSelectComponent extends BaseSelectComponent {
  type: typeof ComponentType.RoleSelect;
  default_values?: DefaultValueRole[];
}

export interface MentionableSelectComponent extends BaseSelectComponent {
  type: typeof ComponentType.MentionableSelect;
  default_values?: Array<DefaultValueUser | DefaultValueRole>;
}

export interface ChannelSelectComponent extends BaseSelectComponent {
  type: typeof ComponentType.ChannelSelect;
  channel_types?: number[];
  default_values?: DefaultValueChannel[];
}

export type SelectComponent =
  | StringSelectComponent
  | UserSelectComponent
  | RoleSelectComponent
  | MentionableSelectComponent
  | ChannelSelectComponent;

